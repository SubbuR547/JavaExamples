package com.java.practice.examples;

public class relationalOperators {

	public static void main(String[] args) {
		int a=47;
		int b=42;
		System.out.println("a==b = "+(a==b));
		System.out.println("a!=b = "+(a!=b));
		System.out.println("a > b = "+(a > b));
		System.out.println("a < b = "+(a < b));
		System.out.println("a>=b = "+(a>=b));
		System.out.println("a<=b = "+(a<=b));
	}

}
